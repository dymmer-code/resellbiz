defmodule Resellbiz do
  @moduledoc """
  Resellbiz is a library that interacts with the Resellbiz API to register,
  transfer, and manage domains, their contacts and everything related to the
  domain business.

  The actions that can be performed, based on the main modules:

  - `Resellbiz.Contact` actions about contact creation for domains.
  - `Resellbiz.Customer` actions for customer management.
  - `Resellbiz.Domain` actions about domain registration, renew, transfer, etc.
  - `Resellbiz.Product` information about products, mainly prices.
  """
end
